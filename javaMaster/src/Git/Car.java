package Git;

public class Car {
String car;
int price;

Car(String car, int price){
	this.car=car;
	this.price=price;
}

public String getCar() {
	return car;
}

public int getPrice() {
	return price;
}

public void setCar(String car) {
	this.car = car;
}

public void setPrice(int price) {
	this.price = price;
}



}
