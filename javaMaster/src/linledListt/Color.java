package linledListt;

public class Color {
	String col;
	String num;
	public Color(String col, String num) {
		super();
		this.col = col;
		this.num = num;
	}
	public String getCol() {
		return col;
	}
	public void setCol(String col) {
		this.col = col;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	
	

}
